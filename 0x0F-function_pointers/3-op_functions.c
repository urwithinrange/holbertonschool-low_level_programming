#include "3-calc.h"
/**
  *op_add - adds 2 numbers
  *@a: first number
  *@b: second number
  *Return: the output of func
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  *op_sub - subtracts 2 numbers
  *@a: first number
  *@b: second number
  *Return: the output of func
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  *op_mul - multiply 2 numbers
  *@a: first number
  *@b: second number
  *Return: the output of func
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  *op_div - divide 2 numbers
  *@a: first number
  *@b: second number
  *Return: the output of func
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  *op_mod - adds 2 numbers
  *@a: first number
  *@b: second number
  *Return: the output of func
  */
int op_mod(int a, int b)
{
	return (a % b);
}
